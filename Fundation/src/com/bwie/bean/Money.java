package com.bwie.bean;

public class Money {
	
	private Integer mid;
	private String mname;
	private String madate;
	private Integer msum;
	private Integer rest;
	private Integer mai;
	private Integer uid;
	public Integer getUid() {
		return uid;
	}
	public void setUid(Integer uid) {
		this.uid = uid;
	}
	public Integer getMai() {
		return mai;
	}
	public void setMai(Integer mai) {
		this.mai = mai;
	}
	public Integer getMid() {
		return mid;
	}
	public void setMid(Integer mid) {
		this.mid = mid;
	}
	public String getMname() {
		return mname;
	}
	public void setMname(String mname) {
		this.mname = mname;
	}


	public String getMadate() {
		return madate;
	}
	public void setMadate(String madate) {
		this.madate = madate;
	}
	public Integer getMsum() {
		return msum;
	}
	public void setMsum(Integer msum) {
		this.msum = msum;
	}
	public Integer getRest() {
		return rest;
	}
	public void setRest(Integer rest) {
		this.rest = rest;
	}
	
	
	
}
